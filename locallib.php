function createsymlink($videoid) {
    global $DB;
   
    $filename = $DB->get_field('local_video_directory', 'filename', [ 'id' => $videoid ]);
   
    if (substr($filename, -4) != '.mp4') {
        $filename .= '.mp4';
    }
  
    $config = get_config('local_video_directory');
    return $config->streaming . "/" . $filename;
}

